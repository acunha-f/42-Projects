#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	temp = lst;
	f(temp->content);
	while (temp->next != NULL)
	{
		f(temp->content);
		temp = temp->next;
	}
}
